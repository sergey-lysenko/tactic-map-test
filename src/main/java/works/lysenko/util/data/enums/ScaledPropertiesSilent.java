package works.lysenko.util.data.enums;

/**
 * Enumeration representing the silent mode for log messages in the ScaledProperties class.
 * <ul>
 *     <li>NO: No log messages will be displayed.</li>
 *     <li>IF_NOT_SCALED: Log messages will be displayed only if scaling is performed.</li>
 *     <li>YES: Log messages will always be displayed.</li>
 * </ul>
 */
@SuppressWarnings("MissingJavadoc")
public enum ScaledPropertiesSilent {

    NO, IF_NOT_SCALED, YES
}
