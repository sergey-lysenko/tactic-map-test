package works.lysenko.util.apis.data;

/**
 * Represents a marker object that holds two descriptors.
 * <p>
 * This interface defines methods for retrieving the first and second descriptors from the marker object.
 */
@SuppressWarnings("InterfaceWithOnlyOneDirectInheritor")
public interface _Marker {

    /**
     * Retrieves the first descriptor from the Marker object.
     *
     * @return the second descriptor
     */
    String getFirst();

    /**
     * Retrieves the second descriptor from the Marker object.
     *
     * @return the second descriptor
     */
    String getSecond();

}
